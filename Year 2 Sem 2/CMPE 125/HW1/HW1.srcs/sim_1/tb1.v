        #10;

        // 0010
        a = 4'b0010;
        #10;

        // 0011
        a = 4'b0011;
        #10;

        // 0100
        a = 4'b0100;
        #10;

        // 0101
        a = 4'b0101;
        #10;

        // 0110
        a = 4'b0110;
        #10;

        // 0111
        a = 4'b0111;
        #10;

        // 1000
        a = 4'b1000;
        #10;

        // 1001
        a = 4'b1001;
        #10;

        // 1010
        a = 4'b1010;
        #10;

        // 1011
        a = 4'b1011;
        #10;

        // 1100
